     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // new instanceof pessoa
        $pessoa         = new Pessoa();
        // buscando o ultimo contaacto cadastrado
        $contactos= Contacto::all();
        $contacto =$contactos->last();
        // ===================================
        // atribuindo dados da pessoa
        $pessoa->nome_completo  =   $request->nome_completo;
        $pessoa->nascimento     =   $request->nascimento;
        $pessoa->contacto_id    =   $contacto->contacto_id;
        // cadastrando a pessoa
        return $pessoa->save();
    }

